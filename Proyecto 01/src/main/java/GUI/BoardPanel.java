    private void paintValidSquares(Graphics2D g) {
        try {
            List<List> validMoves = control.getValidMoves(selectedPieceX, selectedPieceY);
            for (List pos : validMoves) {
                int x = (int) pos.get(0);
                int y = (int) pos.get(1);
                g.drawImage(squareIndicatorSprite,
                        x * ChessController.TILE_SIZE + 3,
                        y * ChessController.TILE_SIZE + 3,
                        null);
            }
        } catch (Exception ex) {
            Logger.getLogger(BoardPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public BoardPanel(GameWindow gameWindow) {
        try {
            initComponents();
            setBounds(0, 0, ChessController.TILE_SIZE * control.getBoardRanks() + 6, ChessController.TILE_SIZE * control.getBoardFiles() + 6);
            pieces = control.getPiecesList();
            this.gameWindow = gameWindow;
            String path = "src/main/resources/graphics/";
            image = ImageIO.read(new FileInputStream(path + "chessboard.png"));
            sprite = image.getScaledInstance(
                    ChessController.TILE_SIZE * control.getBoardRanks() + 6,
                    ChessController.TILE_SIZE * control.getBoardFiles() + 6,
                BufferedImage.SCALE_SMOOTH
            );
            
            image = ImageIO.read(new FileInputStream(path + "boardselection.png"));
            selectionSprite = image.getScaledInstance(ChessController.TILE_SIZE, ChessController.TILE_SIZE, BufferedImage.SCALE_SMOOTH);
            
            image = ImageIO.read(new FileInputStream(path + "validsquareindicator.png"));
            squareIndicatorSprite = image.getScaledInstance(ChessController.TILE_SIZE, ChessController.TILE_SIZE, BufferedImage.SCALE_SMOOTH);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(BoardPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(BoardPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void refresh() {
        control = ChessController.getInstance();
        pieces = control.getPiecesList();
        repaint();
    }
    
    public void deselect() {
        pieceSelected = false;
        repaint();
    }
    
    public void performPromotion(PieceTypes type) {
        control.promotePawn(targetX, targetY, type);
        pieces = control.getPiecesList();
        repaint();
    }
    
    @Override
    public void paintComponent(Graphics g) {
        super.paintComponent(g);
        Graphics2D g2D = (Graphics2D) g;
        g2D.drawImage(sprite, 0, 0, null);
        for (ChessController.GraphicPiece p : pieces) {
            p.paint(g2D);
        }
        if (pieceSelected) {
            g2D.drawImage(selectionSprite,
                    selectedPieceX * ChessController.TILE_SIZE + 3,
                    selectedPieceY * ChessController.TILE_SIZE + 3,
            null);
            paintValidSquares(g2D);
        }
    }
    
