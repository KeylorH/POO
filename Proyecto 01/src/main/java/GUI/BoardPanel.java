    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
        if (!isFocusable()) {
            return;
        }
        Point mousePos = MouseInfo.getPointerInfo().getLocation();
        boardPos = getLocationOnScreen();
        if (!pieceSelected) {
            try {
                selectedPieceX = abs(boardPos.x + 3 - mousePos.x) / ChessController.TILE_SIZE;
                selectedPieceY = abs(boardPos.y + 3 - mousePos.y) / ChessController.TILE_SIZE;
                if (control.doesPieceInSquareBelongToPlayer(selectedPieceX, selectedPieceY)) {
                    //Piece p = control.DEBUG_getPieceInSquare(selectedPieceX, selectedPieceY);
                    //JOptionPane.showMessageDialog(this, String.format(
                    //    "sPieceX: %s, sPieceY: %s\nPiece X: %s, Piece Y: %s",
                    //        selectedPieceX, selectedPieceY, p.getX(), p.getY()
                    //));
                    pieceSelected = true;
                    repaint();
                } else {
                    String n = control.getCurrentPlayerName();
                    JOptionPane.showMessageDialog(this, "It's " + n + "'s turn.");
                }
            } catch (PieceNotFoundException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
                pieceSelected = false;
            }
        } else {
            //int targetX, targetY;
            targetX = abs(boardPos.x + 3 - mousePos.x) / ChessController.TILE_SIZE;
            targetY = abs(boardPos.y + 3 - mousePos.y) / ChessController.TILE_SIZE;
            try {
                control.movePiece(selectedPieceX, selectedPieceY, targetX, targetY);
                pieces = control.getPiecesList();
                pieceSelected = false;
                repaint();
                if (control.isAwaitingPromotion()) {
                    new PromotionWindow(gameWindow).setVisible(true);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
                pieceSelected = false;
            }
        }
    }//GEN-LAST:event_formMousePressed


